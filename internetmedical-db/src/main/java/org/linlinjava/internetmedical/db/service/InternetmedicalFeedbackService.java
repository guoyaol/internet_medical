package org.linlinjava.internetmedical.db.service;

import com.github.pagehelper.PageHelper;
import org.linlinjava.internetmedical.db.dao.InternetmedicalFeedbackMapper;
import org.linlinjava.internetmedical.db.domain.InternetmedicalFeedback;
import org.linlinjava.internetmedical.db.domain.InternetmedicalFeedbackExample;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.stereotype.Service;
import org.springframework.util.StringUtils;

import java.time.LocalDateTime;
import java.util.List;

/**
 * @author Yogeek
 * @date 2018/8/27 11:39
 */
@Service
public class InternetmedicalFeedbackService {
    @Autowired
    private InternetmedicalFeedbackMapper feedbackMapper;

    public Integer add(InternetmedicalFeedback feedback) {
        feedback.setAddTime(LocalDateTime.now());
        feedback.setUpdateTime(LocalDateTime.now());
        return feedbackMapper.insertSelective(feedback);
    }

    public List<InternetmedicalFeedback> querySelective(Integer userId, String username, Integer page, Integer limit, String sort, String order) {
        InternetmedicalFeedbackExample example = new InternetmedicalFeedbackExample();
        InternetmedicalFeedbackExample.Criteria criteria = example.createCriteria();

        if (userId != null) {
            criteria.andUserIdEqualTo(userId);
        }
        if (!StringUtils.isEmpty(username)) {
            criteria.andUsernameLike("%" + username + "%");
        }
        criteria.andDeletedEqualTo(false);

        if (!StringUtils.isEmpty(sort) && !StringUtils.isEmpty(order)) {
            example.setOrderByClause(sort + " " + order);
        }

        PageHelper.startPage(page, limit);
        return feedbackMapper.selectByExample(example);
    }
}
